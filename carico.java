    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
            String pcode = txtcode.getText();
            String newqnt = txtnewqnt.getText();
            String esito = "Positivo";
            
            try {
                
                DbConnection db = new DbConnection();
                int id = db.getId(pcode);
                
                if(id == -1){
                    JOptionPane.showMessageDialog(this, "Il prodotto con questo codice a barre non è in elenco");
                    txtcode.setText("");
                    txtnewqnt.setText("");
                    txtcode.requestFocus();
                }
                else{
                    int newqnty = Integer.parseInt(newqnt);
                    int updt = db.updateQuantity(pcode, newqnty);
                    if(updt==0){
                        JOptionPane.showMessageDialog(this, "impossibile aggiornare la rimanenza");
                        txtcode.setText("");
                        txtnewqnt.setText("");
                        txtcode.requestFocus();
                        esito = "Negativo";
                    }
                    
                    DefaultTableModel model = new DefaultTableModel();
                    model = (DefaultTableModel)jTable1.getModel();
                    Object[] result = db.getProduct(pcode);
                    result[3] = esito;
                    model.insertRow(0,result);

                    txtcode.setText("");
                    txtnewqnt.setText("");
                    txtcode.requestFocus();
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(sales.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(sales.class.getName()).log(Level.SEVERE, null, ex);
            }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    
