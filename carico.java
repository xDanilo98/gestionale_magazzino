class MyNumericVerifier extends InputVerifier {
    @Override
    public boolean verify(JComponent input) {
       String text = ((JTextField) input).getText();
       try {
          int i = Integer.parseInt(text);
          if (i<=0) return false;
       } catch (NumberFormatException e) {
          return false;
       }
       return true;
    }
}

